# Arithmetic Operators in Python
# Arithmetic operators are used to perform mathematical operations on numbers. The common arithmetic operators include:

a = 15 
b = 4
# Addition operator adds two operands
print(f"Addition: {a} + {b} = {a + b}")  
# Subtraction operator subtracts the second operand from the first
print(f"Subtraction: {a} - {b} = {a - b}") 
# Multiplication operator multiplies two operands
print(f"Multiplication: {a} * {b} = {a * b}")
# Division operator divides the first operand by the second
print(f"Division: {a} / {b} = {a / b}") 
# Floor Division operator divides the first operand by the second and returns the quotient as an integer
print(f"Floor Division: {a} // {b} = {a // b}") 
# Modulus operator returns the remainder of the division
print(f"Modulus: {a} % {b} = {a % b}")
# Exponentiation operator raises the first operand to the power of the second operand
print(f"Exponentiation: {a} ** {b} = {a ** b}") 