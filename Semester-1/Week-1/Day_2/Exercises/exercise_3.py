score = int(input("Enter your score: "))
if score >= 90:
    print("Grade: A.","Excellent!🏆")
elif score >= 80:
    print("Grade: B.","Good job!✅")
elif score >= 70:
    print("Grade: C.","Keep going!📘")
elif score >= 60:
    print("Grade: D.","You can do better!⚠️")
else:
    print("Grade: F.","Please revise and try again!❌")