# Comparison Operators
# Comparison operators are used to compare two values and return a boolean result (True or False).
# The common comparison operators include
a = 10
b = 3
print(a == b)  # Equal to
print(a != b) # Not equal to
print(a > b)  # Greater than
print(a < b)  # Less than
print(a >= b) # Greater than or equal to
print(a <= b) # Less than or equal to
print(a is b)  # Identity operator (checks if both operands refer to the same object)
print(a is not b)  # Identity operator (checks if both operands do not refer to the same object)
