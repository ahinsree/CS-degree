# BREAK → in my own words:
# __break is the command that kills the loop immediately.______________________________________________

# CONTINUE → in my own words:
# __continue is the command that skips the rest of the current iteration and moves to the next iteration of the loop.______________________________________________

# PASS → in my own words:
# __pass is the command that does nothing and is used as a placeholder where syntactically some code is required but no action is needed.______________________________________________

# LOOP ELSE → in my own words:
# __loop else executes only if the loop completes normally (i.e., without being terminated by a break statement).______________________________________________

# BREAK vs CONTINUE (key difference):
# __break, it kills the entire loop immediately. continue, it stops the current lap and jumps straight to the start of the next lap._____________________________________________

# NESTED LOOP → when would I use it?
# __Nested Loop as a "loop within a loop." You use it whenever you have a multidimensional problem—meaning you need to track two different things at the same time to get a single result.______________________________________________

